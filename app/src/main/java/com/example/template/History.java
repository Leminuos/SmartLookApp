package com.example.template;

public class History {
    public String name;
    public String type;
    public String time;
    public String date;
    public String ID;

    public History(String name, String type, String time, String date, String ID) {
        this.name = name;
        this.type = type;
        this.time = time;
        this.date = date;
        this.ID = ID;
    }
}
